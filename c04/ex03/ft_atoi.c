int	ft_atoi(char *str)
{
	int		i;
	int		s;
	int		res;

	s = 1;
	res = 0;
	i = 0;
	while (str[i] >= 9 && str[i] <= 13 || str[i] == 32)
	{
		i++;
	}
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			s *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = (res * 10) + (str[i] - '0');
		i++;
	}
	res *= s;
	return (res);
}
